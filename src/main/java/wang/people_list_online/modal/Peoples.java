package wang.people_list_online.modal;

public class Peoples {
    private String peoplesId;

    private String peoplesName;

    private String peoplesSex;

    private Integer peoplesAge;

    public String getPeoplesId() {
        return peoplesId;
    }

    public void setPeoplesId(String peoplesId) {
        this.peoplesId = peoplesId;
    }

    public String getPeoplesName() {
        return peoplesName;
    }

    public void setPeoplesName(String peoplesName) {
        this.peoplesName = peoplesName;
    }

    public String getPeoplesSex() {
        return peoplesSex;
    }

    public void setPeoplesSex(String peoplesSex) {
        this.peoplesSex = peoplesSex;
    }

    public Integer getPeoplesAge() {
        return peoplesAge;
    }

    public void setPeoplesAge(Integer peoplesAge) {
        this.peoplesAge = peoplesAge;
    }
}